/**
 * Export a simple table to a printable HTML page (opens print dialog).
 */
export function exportToPrint<T>(
  data: T[],
  columns: ExportColumn<T>[],
  title: string
): void {
  const headerRow = columns.map(c => `<th style="padding:8px 12px;text-align:left;border-bottom:2px solid #333;font-size:12px;font-weight:600;color:#333">${c.header}</th>`).join('')
  const bodyRows = data.map(row => {
    const cells = columns.map(col => {
      const val = col.accessor(row)
      return `<td style="padding:6px 12px;border-bottom:1px solid #eee;font-size:11px;color:#555">${val == null ? '' : String(val)}</td>`
    }).join('')
    return `<tr>${cells}</tr>`
  }).join('')

  const html = `<!DOCTYPE html>
<html><head>
<title>${title} - Tempo Export</title>
<style>body{font-family:-apple-system,BlinkMacSystemFont,sans-serif;padding:20px}table{border-collapse:collapse;width:100%}@media print{.no-print{display:none}}</style>
</head><body>
<div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:20px">
  <div>
    <h1 style="margin:0;font-size:18px;color:#111">${title}</h1>
    <p style="margin:4px 0 0;font-size:12px;color:#999">Exported ${new Date().toLocaleDateString()} | ${data.length} records | Tempo Platform</p>
  </div>
  <button class="no-print" onclick="window.print()" style="padding:8px 16px;background:#ea580c;color:#fff;border:none;border-radius:6px;cursor:pointer;font-size:12px">Print / Save as PDF</button>
</div>
<table>
  <thead><tr>${headerRow}</tr></thead>
  <tbody>${bodyRows}</tbody>
</table>
<div style="margin-top:20px;text-align:center">
  <p style="font-size:10px;color:#bbb">Generated by Tempo - Unified Workforce Platform</p>
</div>
</body></html>`

  const printWindow = window.open('', '_blank')
  if (printWindow) {
    printWindow.document.write(html)
    printWindow.document.close()
  }
}

// ============================================================
// CSV IMPORT / PARSING
// ============================================================

export interface ParsedCSV {
  headers: string[]
  rows: Record<string, string>[]
  errors: string[]
  totalRows: number
}

/**
 * Parse a CSV string into structured data.
 */
export function parseCSV(csvText: string): ParsedCSV {
  const errors: string[] = []
  const lines = csvText.split(/\r?\n/).filter(line => line.trim().length > 0)

  if (lines.length === 0) {
    return { headers: [], rows: [], errors: ['Empty CSV file'], totalRows: 0 }
  }

  const headers = parseCSVLine(lines[0]).map(h => h.trim())
  if (headers.length === 0) {
    return { headers: [], rows: [], errors: ['No headers found in CSV'], totalRows: 0 }
  }

  const rows: Record<string, string>[] = []
  for (let i = 1; i < lines.length; i++) {
    const values = parseCSVLine(lines[i])
    if (values.length !== headers.length) {
      errors.push(`Row ${i}: Expected ${headers.length} columns, got ${values.length}`)
      continue
    }
    const row: Record<string, string> = {}
    headers.forEach((header, idx) => {
      row[header] = values[idx]?.trim() || ''
    })
    rows.push(row)
  }

  return { headers, rows, errors, totalRows: lines.length - 1 }
}

function parseCSVLine(line: string): string[] {
  const result: string[] = []
  let current = ''
  let inQuotes = false

  for (let i = 0; i < line.length; i++) {
    const ch = line[i]
    if (inQuotes) {
      if (ch === '"') {
        if (i + 1 < line.length && line[i + 1] === '"') {
          current += '"'
          i++
        } else {
          inQuotes = false
        }
      } else {
        current += ch
      }
    } else {
      if (ch === '"') {
        inQuotes = true
      } else if (ch === ',') {
        result.push(current)
        current = ''
      } else {
        current += ch
      }
    }
  }
  result.push(current)
  return result
}

/**
 * Read a File object as CSV text.
 */
export function readFileAsCSV(file: File): Promise<ParsedCSV> {
  return new Promise((resolve, reject) => {
    const reader = new FileReader()
    reader.onload = (e) => {
      const text = e.target?.result as string
      resolve(parseCSV(text))
    }
    reader.onerror = () => reject(new Error('Failed to read file'))
    reader.readAsText(file)
  })
}

