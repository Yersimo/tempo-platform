// CSV IMPORT / PARSING
// ============================================================

export interface ParsedCSV {
  headers: string[]
  rows: Record<string, string>[]
  errors: string[]
  totalRows: number
}

/**
 * Parse a CSV string into structured data.
 */
export function parseCSV(csvText: string): ParsedCSV {
  const errors: string[] = []
  const lines = csvText.split(/\r?\n/).filter(line => line.trim().length > 0)

  if (lines.length === 0) {
    return { headers: [], rows: [], errors: ['Empty CSV file'], totalRows: 0 }
  }

  const headers = parseCSVLine(lines[0]).map(h => h.trim())
  if (headers.length === 0) {
    return { headers: [], rows: [], errors: ['No headers found in CSV'], totalRows: 0 }
  }

  const rows: Record<string, string>[] = []
  for (let i = 1; i < lines.length; i++) {
    const values = parseCSVLine(lines[i])
    if (values.length !== headers.length) {
      errors.push(`Row ${i}: Expected ${headers.length} columns, got ${values.length}`)
      continue
    }
    const row: Record<string, string> = {}
    headers.forEach((header, idx) => {
      row[header] = values[idx]?.trim() || ''
    })
    rows.push(row)
  }

  return { headers, rows, errors, totalRows: lines.length - 1 }
}

function parseCSVLine(line: string): string[] {
  const result: string[] = []
  let current = ''
  let inQuotes = false

  for (let i = 0; i < line.length; i++) {
    const ch = line[i]
    if (inQuotes) {
      if (ch === '"') {
        if (i + 1 < line.length && line[i + 1] === '"') {
          current += '"'
          i++
        } else {
          inQuotes = false
        }
      } else {
        current += ch
      }
    } else {
      if (ch === '"') {
        inQuotes = true
      } else if (ch === ',') {
        result.push(current)
        current = ''
      } else {
        current += ch
      }
    }
  }
  result.push(current)
  return result
}

/**
 * Read a File object as CSV text.
 */
export function readFileAsCSV(file: File): Promise<ParsedCSV> {
  return new Promise((resolve, reject) => {
    const reader = new FileReader()
    reader.onload = (e) => {
      const text = e.target?.result as string
      resolve(parseCSV(text))
    }
    reader.onerror = () => reject(new Error('Failed to read file'))
    reader.readAsText(file)
  })
}

