/**
 * Export a simple table to a printable HTML page (opens print dialog).
 */
export function exportToPrint<T>(
  data: T[],
  columns: ExportColumn<T>[],
  title: string
): void {
  const headerRow = columns.map(c => `<th style="padding:8px 12px;text-align:left;border-bottom:2px solid #333;font-size:12px;font-weight:600;color:#333">${c.header}</th>`).join('')
  const bodyRows = data.map(row => {
    const cells = columns.map(col => {
      const val = col.accessor(row)
      return `<td style="padding:6px 12px;border-bottom:1px solid #eee;font-size:11px;color:#555">${val == null ? '' : String(val)}</td>`
    }).join('')
    return `<tr>${cells}</tr>`
  }).join('')

  const html = `<!DOCTYPE html>
<html><head>
<title>${title} - Tempo Export</title>
<style>body{font-family:-apple-system,BlinkMacSystemFont,sans-serif;padding:20px}table{border-collapse:collapse;width:100%}@media print{.no-print{display:none}}</style>
</head><body>
<div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:20px">
  <div>
    <h1 style="margin:0;font-size:18px;color:#111">${title}</h1>
    <p style="margin:4px 0 0;font-size:12px;color:#999">Exported ${new Date().toLocaleDateString()} | ${data.length} records | Tempo Platform</p>
  </div>
  <button class="no-print" onclick="window.print()" style="padding:8px 16px;background:#ea580c;color:#fff;border:none;border-radius:6px;cursor:pointer;font-size:12px">Print / Save as PDF</button>
</div>
<table>
  <thead><tr>${headerRow}</tr></thead>
  <tbody>${bodyRows}</tbody>
</table>
<div style="margin-top:20px;text-align:center">
  <p style="font-size:10px;color:#bbb">Generated by Tempo - Unified Workforce Platform</p>
</div>
</body></html>`

  const printWindow = window.open('', '_blank')
  if (printWindow) {
    printWindow.document.write(html)
    printWindow.document.close()
  }
}

