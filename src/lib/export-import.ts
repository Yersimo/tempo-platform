/**
 * Export / Import Utilities
 *
 * Client-safe functions for exporting data to CSV/JSON and parsing CSV imports.
 * No database imports -- safe for 'use client' pages.
 */

// ============================================================
// CSV EXPORT
// ============================================================

interface ExportColumn<T> {
  header: string
  accessor: (row: T) => string | number | boolean | null | undefined
}

/**
 * Export an array of objects to CSV and trigger a browser download.
 */
export function exportToCSV<T>(
  data: T[],
  columns: ExportColumn<T>[],
  filename: string
): void {
  if (data.length === 0) return

  const headers = columns.map(c => escapeCSVField(c.header))
  const rows = data.map(row =>
    columns.map(col => {
      const val = col.accessor(row)
      return escapeCSVField(val == null ? '' : String(val))
    })
  )

  const csvContent = [headers.join(','), ...rows.map(r => r.join(','))].join('\n')
  downloadFile(csvContent, `${filename}.csv`, 'text/csv;charset=utf-8;')
}

function escapeCSVField(value: string): string {
  if (value.includes(',') || value.includes('"') || value.includes('\n')) {
    return `"${value.replace(/"/g, '""')}"`
  }
  return value
}

// ============================================================
// JSON EXPORT
// ============================================================

/**
 * Export data to JSON and trigger a browser download.
 */
export function exportToJSON<T>(data: T[], filename: string): void {
  const jsonContent = JSON.stringify(data, null, 2)
  downloadFile(jsonContent, `${filename}.json`, 'application/json')
}

