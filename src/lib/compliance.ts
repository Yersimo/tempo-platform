export function getApplicableRules(countries: string[]): LaborLawRule[] {
  const jurisdictions = new Set<string>()

  for (const country of countries) {
    // Map country names/codes to jurisdiction codes
    const upper = country.trim().toUpperCase()
    if (upper === 'US' || upper === 'USA' || upper === 'UNITED STATES') jurisdictions.add('US')
    if (upper.startsWith('US-') || upper === 'CALIFORNIA') jurisdictions.add('US-CA')
    if (upper === 'US-NY' || upper === 'NEW YORK') jurisdictions.add('US-NY')
    if (upper === 'UK' || upper === 'GB' || upper === 'UNITED KINGDOM') jurisdictions.add('UK')
    if (upper === 'DE' || upper === 'GERMANY') jurisdictions.add('DE')
    if (upper === 'FR' || upper === 'FRANCE') jurisdictions.add('FR')
    // Add US federal for any US state
    if (upper.startsWith('US') || upper === 'USA' || upper === 'UNITED STATES') jurisdictions.add('US')

    // Map African countries
    const africanCode = AFRICAN_COUNTRY_MAP[upper]
    if (africanCode) {
      jurisdictions.add(africanCode)
    }
  }

  return LABOR_LAW_RULES.filter(r => jurisdictions.has(r.jurisdiction))
}

export async function scanCompliance(orgId: string): Promise<{
  violations: ComplianceViolation[]
  applicableRules: LaborLawRule[]
  score: number
  summary: { total: number; critical: number; warnings: number; info: number }
}> {
  // Get all employee countries
  const employees = await db.select({
    id: schema.employees.id,
    country: schema.employees.country,
    jobTitle: schema.employees.jobTitle,
    isActive: schema.employees.isActive,
  }).from(schema.employees).where(eq(schema.employees.orgId, orgId))

  const countries = [...new Set(employees.map(e => e.country).filter(Boolean) as string[])]
  const rules = getApplicableRules(countries)
  const violations: ComplianceViolation[] = []

  // Check for common violations
  // 1. Missing job titles (needed for EEO)
  const missingTitles = employees.filter(e => e.isActive && !e.jobTitle)
  if (missingTitles.length > 0) {
    violations.push({
      id: `v-missing-titles-${orgId}`,
      ruleId: 'us-fed-eeo',
      severity: 'warning',
      title: 'Missing Job Titles',
      description: `${missingTitles.length} active employees lack job titles, required for EEO reporting.`,
      remediation: 'Update employee records to include job titles via People > Edit Employee.',
      detectedAt: new Date().toISOString(),
    })
  }

  // 2. Check leave request compliance
  const leaveRequests = await db.select().from(schema.leaveRequests)
    .where(eq(schema.leaveRequests.orgId, orgId))
  const rejectedLeave = leaveRequests.filter(l => l.status === 'rejected')
  if (rejectedLeave.length > leaveRequests.length * 0.3 && leaveRequests.length > 5) {
    violations.push({
      id: `v-leave-rejection-rate-${orgId}`,
      ruleId: 'us-fed-fmla',
      severity: 'warning',
      title: 'High Leave Rejection Rate',
      description: `${Math.round((rejectedLeave.length / leaveRequests.length) * 100)}% of leave requests rejected. Review for FMLA compliance.`,
      remediation: 'Audit rejected leave requests to ensure no protected leave was improperly denied.',
      detectedAt: new Date().toISOString(),
    })
  }

  // 3. Check for pay equity issues
  const salaryReviews = await db.select().from(schema.salaryReviews)
    .where(eq(schema.salaryReviews.orgId, orgId))
  const pendingSalary = salaryReviews.filter(s => s.status === 'pending_approval')
  if (pendingSalary.length > 10) {
    violations.push({
      id: `v-pending-comp-${orgId}`,
      ruleId: 'us-ca-pay-transparency',
      severity: 'info',
      title: 'Pending Compensation Reviews',
      description: `${pendingSalary.length} salary reviews awaiting approval. Delays may impact pay equity compliance.`,
      remediation: 'Process pending salary reviews in Compensation module.',
      detectedAt: new Date().toISOString(),
    })
  }

  // 4. Minimum wage violation check (all countries in labor law registry)
  const activeEmployees = employees.filter(e => e.isActive)
  for (const emp of activeEmployees) {
    if (!emp.country) continue
    const countryCode = AFRICAN_COUNTRY_MAP[emp.country.trim().toUpperCase()] || emp.country.trim().toUpperCase()
    const minWage = getMinimumWage(countryCode)
    if (!minWage || minWage.annual <= 0) continue

    // Look up latest salary
    const latestReview = await db.select({
      currentSalary: schema.salaryReviews.currentSalary,
      proposedSalary: schema.salaryReviews.proposedSalary,
      status: schema.salaryReviews.status,
      currency: schema.salaryReviews.currency,
    }).from(schema.salaryReviews)
      .where(and(eq(schema.salaryReviews.employeeId, emp.id), eq(schema.salaryReviews.orgId, orgId)))
      .orderBy(desc(schema.salaryReviews.createdAt))
      .limit(1)

    if (latestReview.length === 0) continue
    const review = latestReview[0]
    const salary = review.status === 'approved' ? review.proposedSalary : review.currentSalary
    if (!salary || salary <= 0) continue

    // Simple check: if salary currency matches minimum wage currency, compare directly
    // (Cross-currency comparison happens in payroll engine's validatePayrollCompliance)
    const salCurrency = (review.currency || 'USD').toUpperCase()
    if (salCurrency === minWage.currency.toUpperCase() && salary < minWage.annual) {
      violations.push({
        id: `v-min-wage-${emp.id}`,
        ruleId: `${countryCode.toLowerCase()}-min-wage`,
        severity: 'critical',
        title: 'Below Minimum Wage',
        description: `Employee salary (${salary.toLocaleString()} ${minWage.currency}/year) is below the statutory minimum wage (${minWage.annual.toLocaleString()} ${minWage.currency}/year) for ${countryCode}.`,
        remediation: `Increase salary to at least ${minWage.annual.toLocaleString()} ${minWage.currency} per year to comply with ${countryCode} minimum wage law.`,
        detectedAt: new Date().toISOString(),
      })
    }
  }

  const critical = violations.filter(v => v.severity === 'critical').length
  const warnings = violations.filter(v => v.severity === 'warning').length
  const info = violations.filter(v => v.severity === 'info').length
  const score = Math.max(0, 100 - (critical * 20) - (warnings * 5) - (info * 1))

  return {
    violations,
    applicableRules: rules,
    score,
    summary: { total: violations.length, critical, warnings, info },
  }
}

