export interface EEOReport {
  reportDate: string
  orgId: string
  totalEmployees: number
  categories: Array<{
    category: string
    count: number
    percentage: number
  }>
  jobCategories: Array<{
    title: string
    count: number
  }>
  departmentBreakdown: Array<{
    department: string
    count: number
  }>
}

export async function generateEEOReport(orgId: string): Promise<EEOReport> {
  const employees = await db.select().from(schema.employees)
    .where(and(eq(schema.employees.orgId, orgId), eq(schema.employees.isActive, true)))
  const departments = await db.select().from(schema.departments)
    .where(eq(schema.departments.orgId, orgId))

  const deptMap = new Map(departments.map(d => [d.id, d.name]))

  // Job category counts
  const jobCounts = new Map<string, number>()
  const deptCounts = new Map<string, number>()

  for (const emp of employees) {
    const title = emp.jobTitle || 'Unspecified'
    jobCounts.set(title, (jobCounts.get(title) || 0) + 1)
    const dept = emp.departmentId ? deptMap.get(emp.departmentId) || 'Unassigned' : 'Unassigned'
    deptCounts.set(dept, (deptCounts.get(dept) || 0) + 1)
  }

  // Role distribution
  const roleCounts = new Map<string, number>()
  for (const emp of employees) {
    roleCounts.set(emp.role, (roleCounts.get(emp.role) || 0) + 1)
  }

  return {
    reportDate: new Date().toISOString(),
    orgId,
    totalEmployees: employees.length,
    categories: Array.from(roleCounts.entries()).map(([category, count]) => ({
      category,
      count,
      percentage: Math.round((count / employees.length) * 100),
    })),
    jobCategories: Array.from(jobCounts.entries())
      .sort((a, b) => b[1] - a[1])
      .slice(0, 20)
      .map(([title, count]) => ({ title, count })),
    departmentBreakdown: Array.from(deptCounts.entries())
      .sort((a, b) => b[1] - a[1])
      .map(([department, count]) => ({ department, count })),
  }
}
